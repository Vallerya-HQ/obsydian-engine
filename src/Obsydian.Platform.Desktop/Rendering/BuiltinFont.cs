            '{' => [0x02, 0x04, 0x08, 0x04, 0x04, 0x02, 0x00],
            '|' => [0x04, 0x04, 0x04, 0x04, 0x04, 0x04, 0x00],
            '}' => [0x08, 0x04, 0x02, 0x04, 0x04, 0x08, 0x00],
            '~' => [0x00, 0x00, 0x08, 0x15, 0x02, 0x00, 0x00],
            _ => [0x1F, 0x11, 0x11, 0x11, 0x11, 0x1F, 0x00], // box for unknown

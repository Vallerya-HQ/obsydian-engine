using Obsydian.Core.Math;
using Obsydian.Graphics;
using Silk.NET.OpenGL;
using Texture = Obsydian.Graphics.Texture;

namespace Obsydian.Platform.Desktop.Rendering;

/// <summary>
/// Generates a simple 5x7 pixel bitmap font at runtime. No font files needed.
/// Covers ASCII 32-126 (printable characters: space through ~).
/// </summary>
public static class BuiltinFont
{
    private const int GlyphW = 5;
    private const int GlyphH = 7;
    private const int Cols = 16;
    private const int Padding = 1;
    private const int CellW = GlyphW + Padding;
    private const int CellH = GlyphH + Padding;

    public static BitmapFont Create(GL gl)
    {
        int rows = (95 + Cols - 1) / Cols; // 95 printable chars
        int texW = Cols * CellW;
        int texH = rows * CellH;
        var pixels = new byte[texW * texH * 4];

        for (int i = 0; i < 95; i++)
        {
            char c = (char)(32 + i);
            int col = i % Cols;
            int row = i / Cols;
            int originX = col * CellW;
            int originY = row * CellH;

            var bitmap = GetGlyphBitmap(c);
            for (int py = 0; py < GlyphH; py++)
            for (int px = 0; px < GlyphW; px++)
            {
                if (bitmap[py * GlyphW + px] != 0)
                {
                    int idx = ((originY + py) * texW + (originX + px)) * 4;
                    pixels[idx] = 255;
                    pixels[idx + 1] = 255;
                    pixels[idx + 2] = 255;
                    pixels[idx + 3] = 255;
                }
            }
        }

        var texture = GlTexture.Create(gl, texW, texH, pixels, "__builtin_font");
        var font = new BitmapFont(texture, GlyphH);

        for (int i = 0; i < 95; i++)
        {
            char c = (char)(32 + i);
            int col = i % Cols;
            int row = i / Cols;
            var src = new Rect(col * CellW, row * CellH, GlyphW, GlyphH);
            font.AddGlyph(c, src, GlyphW);
        }

        return font;
    }

    private static byte[] GetGlyphBitmap(char c)
    {
        // 5x7 pixel font bitmaps for printable ASCII
        // Each row is encoded as a hex byte (5 bits used), 7 rows per glyph
        byte[] rows = c switch

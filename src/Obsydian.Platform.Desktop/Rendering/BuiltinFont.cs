            'G' => [0x0E, 0x11, 0x10, 0x17, 0x11, 0x0E, 0x00],
            'H' => [0x11, 0x11, 0x1F, 0x11, 0x11, 0x11, 0x00],
            'I' => [0x0E, 0x04, 0x04, 0x04, 0x04, 0x0E, 0x00],
            'J' => [0x07, 0x02, 0x02, 0x02, 0x12, 0x0C, 0x00],
            'K' => [0x11, 0x12, 0x14, 0x18, 0x14, 0x12, 0x11],

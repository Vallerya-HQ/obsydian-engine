            'f' => [0x02, 0x04, 0x0E, 0x04, 0x04, 0x04, 0x00],
            'g' => [0x00, 0x0F, 0x11, 0x0F, 0x01, 0x0E, 0x00],
            'h' => [0x10, 0x10, 0x1E, 0x11, 0x11, 0x11, 0x00],
            'i' => [0x04, 0x00, 0x04, 0x04, 0x04, 0x04, 0x00],
            'j' => [0x02, 0x00, 0x02, 0x02, 0x12, 0x0C, 0x00],
            'k' => [0x10, 0x12, 0x14, 0x18, 0x14, 0x12, 0x00],
            'l' => [0x0C, 0x04, 0x04, 0x04, 0x04, 0x0E, 0x00],
            'm' => [0x00, 0x00, 0x1A, 0x15, 0x15, 0x11, 0x00],
            'n' => [0x00, 0x00, 0x1E, 0x11, 0x11, 0x11, 0x00],
            'o' => [0x00, 0x00, 0x0E, 0x11, 0x11, 0x0E, 0x00],
            'p' => [0x00, 0x1E, 0x11, 0x1E, 0x10, 0x10, 0x00],
            'q' => [0x00, 0x0F, 0x11, 0x0F, 0x01, 0x01, 0x00],
            'r' => [0x00, 0x00, 0x16, 0x18, 0x10, 0x10, 0x00],
            's' => [0x00, 0x0E, 0x10, 0x0E, 0x01, 0x1E, 0x00],
            't' => [0x04, 0x04, 0x0E, 0x04, 0x04, 0x02, 0x00],
            'u' => [0x00, 0x00, 0x11, 0x11, 0x11, 0x0F, 0x00],
            'v' => [0x00, 0x00, 0x11, 0x11, 0x0A, 0x04, 0x00],
            'w' => [0x00, 0x00, 0x11, 0x15, 0x15, 0x0A, 0x00],

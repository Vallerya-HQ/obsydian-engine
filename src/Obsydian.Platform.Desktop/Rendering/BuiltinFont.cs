        var bitmap = new byte[GlyphW * GlyphH];
        for (int y = 0; y < GlyphH; y++)
        {
            byte row = rows[y];
            for (int x = 0; x < GlyphW; x++)
            {
                // Bit 4 is leftmost pixel, bit 0 is rightmost
                bitmap[y * GlyphW + x] = (byte)((row >> (4 - x)) & 1);
            }
        }
        return bitmap;
    }
}
